    private void btnStudentLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStudentLoginActionPerformed
        // TODO add your handling code here:
        String sql = "select * from Student_login where username = ? and password = ? ";
        try {
            pst = connection.prepareStatement(sql);
            pst.setString(1, txtStudentUsername.getText());
            pst.setString(2, txtStudentPassword.getText());
            resultSet = pst.executeQuery();

            if (resultSet.next()) {
                close();
                StudentArea obj = new StudentArea();
                obj.setVisible(true);
                try {
                    resultSet.close();
                    pst.close();
                    connection.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, e);
                }

            } else {

                JOptionPane.showMessageDialog(rootPane, "Username or Password is incorrect");

            }
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }

    }//GEN-LAST:event_btnStudentLoginActionPerformed

