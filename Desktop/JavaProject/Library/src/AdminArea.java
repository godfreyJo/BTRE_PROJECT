    private void Update_Table() {
        try {
            String sql = "select * from Student_reg";
            pst = connection.prepareStatement(sql);
            resultSet = pst.executeQuery();
            tableRegistration.setModel(DbUtils.resultSetToTableModel(resultSet));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    private void getValue() {
        try {
            txtSDStudentId.setText(resultSet.getString("ID"));
            txtSDFname.setText(resultSet.getString("F_name"));
            txtSDLname.setText(resultSet.getString("L_name"));
            txtSDFaculty.setText(resultSet.getString("Faculty"));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }

    }
    
