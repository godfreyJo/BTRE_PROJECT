    private void tableRegistrationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableRegistrationMouseClicked
        // TODO add your handling code here:
        try {

            int row = tableRegistration.getSelectedRow();
            String tableClick = (tableRegistration.getModel().getValueAt(row, 0)).toString();
            String sql = "select ID, F_name,L_name,Faculty from Student_reg where ID = '" + tableClick + "'";
            pst = connection.prepareStatement(sql);
            resultSet = pst.executeQuery();
            if (resultSet.next()) {
                getValue();

            }

        } catch (Exception e) {
        }

    }//GEN-LAST:event_tableRegistrationMouseClicked

    private void btnAddStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStudentActionPerformed
        // TODO add your handling code here:
        String sql = "insert into Registered_Students(Student_Id,F_name,L_name,Faculty)values (?,?,?,?)";
        try {
            pst = connection.prepareStatement(sql);
            pst.setString(1, txtSDStudentId.getText());
            pst.setString(2, txtSDFname.getText());
            pst.setString(3, txtSDLname.getText());
            pst.setString(4, txtSDFaculty.getText());
            pst.execute();

            JOptionPane.showMessageDialog(rootPane, "Saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        updatetableRegisteredStudents();
    }//GEN-LAST:event_btnAddStudentActionPerformed

