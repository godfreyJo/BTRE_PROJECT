    private void btnStudentUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStudentUploadActionPerformed
        // TODO add your handling code here:
        
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        ///file chooser  code
        File f = chooser.getSelectedFile();
        String fileName = f.getAbsolutePath();
        txtStudentImageUpload.setText(fileName);

        try {
            FileInputStream fIS = new FileInputStream(f);
            ByteArrayOutputStream bAOS = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            for (int readNum; (readNum = fIS.read(buf)) != 1;) {

                bAOS.write(buf, 0, readNum);

            }

            byte[] personImg = bAOS.toByteArray();

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnStudentUploadActionPerformed

    private void btnStudentSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStudentSubmitActionPerformed
        // TODO add your handling code here:
        String sql = "insert into Student_reg(ID,F_name,L_name,Gender,Campus, Faculty,Email)values(?,?,?,?,?,?,?)";
        try {
            pst = connection.prepareStatement(sql);
            pst.setString(1,txtStudentId.getText());
            pst.setString(2,txtStudRegFname.getText());
            pst.setString(3,txtStudRegLname.getText());
            pst.setString(4,(String)comboGender.getSelectedItem());
            pst.setString(5,(String) comboCampus.getSelectedItem());
            pst.setString(6,(String)comboFaculty.getSelectedItem());
            pst.setString(7,txtStudRegEmailAdd.getText()); 
            pst.execute();
            
            JOptionPane.showMessageDialog(rootPane,"Submited");
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane,e);
        }
        
        
    }//GEN-LAST:event_btnStudentSubmitActionPerformed

